show_section_header() {
  local title="$1"
  local width
  width=$(get_term_width)
  local box_w=$((width > 70 ? 70 : width - 4))
  [[ $box_w -lt 40 ]] && box_w=40
  local inner=$((box_w - 2))
  local line
  line=$(printf 'â•%.0s' $(seq 1 "$inner"))
  local title_text="$title"
  local title_visual_w
  title_visual_w=$(_visual_width "$title_text")
  local pad=$((inner - 2 - title_visual_w))
  if [[ $pad -lt 0 ]]; then
    title_text="${title_text:0:$((inner - 5))}..."
    title_visual_w=$(_visual_width "$title_text")
    pad=$((inner - 2 - title_visual_w))
    [[ $pad -lt 0 ]] && pad=0
  fi

  echo ""
  echo -e "${BANNER_CYAN}â•”${line}â•—${BANNER_RESET}"
  printf "${BANNER_CYAN}â•‘${BANNER_RESET}  ${BANNER_BOLD}%s${BANNER_RESET}%*s${BANNER_CYAN}â•‘${BANNER_RESET}\n" "$title_text" "$pad" ""
  echo -e "${BANNER_CYAN}â•š${line}â•${BANNER_RESET}"
  echo ""
}

